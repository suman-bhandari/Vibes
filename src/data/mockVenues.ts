// Helper function to determine activity level
function getActivityLevel(capacity: number, waitTime: number): ActivityLevel {
  if (capacity >= 90 || waitTime >= 60) return 'very-busy';
  if (capacity >= 60 || waitTime >= 30) return 'moderately-busy';
  if (capacity >= 30 || waitTime >= 15) return 'some-activity';
  return 'available';
}

// Helper function to create wait time interval (85% confidence, ~5 min range)
function getWaitTimeInterval(baseWaitTime: number): [number, number] {
  const range = 5; // ~5 minutes range
  const min = Math.max(0, Math.floor(baseWaitTime - range / 2));
  const max = Math.ceil(baseWaitTime + range / 2);
  return [min, max];
}

